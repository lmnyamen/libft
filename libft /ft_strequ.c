#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int p;

	if (s1 == '\0' || s2 == '\0')
		return (0);
	p = ft_strcmp((char *)s1, (char *)s2);
	if (p == 0)
		return (1);
	return (0);
}
