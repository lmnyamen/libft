#include "libft.h"

void		*ft_memset(void *b, int c, size_t len)
{
	char	*test;
	int		i;

	test = b;
	i = 0;
	while (len > 0)
	{
		test[i] = c;
		i++;
		len--;
	}
	return (b);
}
