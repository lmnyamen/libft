#include "libft.h"

void		ft_striter(char *s, void (*f)(char *))
{
	int i;

	i = 0;
	if (!s || !f)
		return ;
	if (s != NULL)
	{
		while (s[i])
		{
			(f)(&s[i]);
			i++;
		}
		return ;
	}
}
