#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int				i;
	unsigned int	l;

	i = 0;
	l = 0;
	while (s1[i])
		i++;
	while (s2[l] && (l < n))
	{
		s1[i] = s2[l];
		i++;
		l++;
	}
	s1[i] = '\0';
	return (s1);
}
