#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int p;

	if (s1 == '\0' || s2 == '\0')
		return (0);
	p = ft_strncmp(s1, s2, n);
	if (p == 0)
		return (1);
	return (0);
}
